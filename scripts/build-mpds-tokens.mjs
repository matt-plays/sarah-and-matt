/**
 * Generates node_modules/@mattplays/mpds/dist/ from token source on GitHub.
 * Runs automatically via `postinstall`.
 */

import { mkdirSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const DIST = resolve(ROOT, 'node_modules/@mattplays/mpds/dist');
const BASE = 'https://raw.githubusercontent.com/matt-plays/mpds/main/tokens';

// ── Helpers ─────────────────────────────────────────────────────────────────

function slugify(name) {
  return name.trim().toLowerCase().replace(/\s+/g, '-');
}

function header(title) {
  return `/* MPDS — ${title} */\n/* Auto-generated by scripts/build-mpds-tokens.mjs */\n\n`;
}

function write(path, content) {
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, content, 'utf-8');
  console.log(`  ✓ ${path.replace(ROOT + '/', '')}`);
}

async function fetchJSON(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`${res.status} ${res.statusText} — ${url}`);
  return res.json();
}

function fluidValue(mobile, desktop, mVW = 480, dVW = 1440) {
  if (mobile === desktop) return `${desktop}px`;
  const min = Math.min(mobile, desktop);
  const max = Math.max(mobile, desktop);
  const slope = (desktop - mobile) / (dVW - mVW);
  const intercept = mobile - slope * mVW;
  const slopeVW = +(slope * 100).toFixed(4);
  const interceptPx = +intercept.toFixed(4);
  const preferred = interceptPx === 0
    ? `${slopeVW}vw`
    : `${interceptPx}px + ${slopeVW}vw`;
  return `clamp(${min}px, ${preferred}, ${max}px)`;
}

const TSHIRT = { '2xs': '12', xs: '16', sm: '32', md: '48', lg: '80', xl: '128', '2xl': '208' };

// ── Fetch all token sources in parallel ─────────────────────────────────────

console.log('Fetching MPDS tokens from GitHub…');

const [colors, mobile, tablet, desktop] = await Promise.all([
  fetchJSON(`${BASE}/core.tokens.json`),
  fetchJSON(`${BASE}/metrics-mobile.tokens.json`),
  fetchJSON(`${BASE}/metrics-tablet.tokens.json`),
  fetchJSON(`${BASE}/metrics-desktop.tokens.json`),
]);

// ── Build: Colors ────────────────────────────────────────────────────────────

const colorLines = [];
for (const [groupName, groupTokens] of Object.entries(colors)) {
  if (groupName.startsWith('$')) continue;
  const base = groupTokens.$base;
  for (const [tokenName, token] of Object.entries(groupTokens)) {
    if (tokenName.startsWith('$')) continue;
    if (typeof token !== 'object' || token.$type !== 'color') continue;
    const { hex, alpha } = token.$value;
    const value = alpha < 1 && base
      ? `color-mix(in srgb, var(--mpds-color-${base}) ${Math.round(alpha * 100)}%, transparent)`
      : hex;
    colorLines.push(`  --mpds-color-${slugify(tokenName)}: ${value};`);
  }
  colorLines.push('');
}

const colorsCss = header('Color Tokens') + `:root {\n${colorLines.join('\n')}\n}\n`;

// ── Build: Metrics (fluid) ───────────────────────────────────────────────────

const fluidLines = [];

fluidLines.push('  /* Font Size */');
for (const key of Object.keys(desktop['font-size'])) {
  if (key.startsWith('$')) continue;
  const step = key.replace(/^font-size-?/, '');
  const mVal = mobile['font-size'][key]?.$value;
  const dVal = desktop['font-size'][key]?.$value;
  if (mVal == null || dVal == null) continue;
  fluidLines.push(`  --mpds-font-size-${step}: ${fluidValue(mVal, dVal)};`);
}
fluidLines.push('');

const numericSteps = [];
fluidLines.push('  /* Spacing */');
for (const key of Object.keys(desktop.space)) {
  if (key.startsWith('$') || !/^space-\d+$/.test(key)) continue;
  const step = key.replace(/^space-/, '');
  numericSteps.push(step);
  const mVal = mobile.space[key]?.$value;
  const dVal = desktop.space[key]?.$value;
  if (mVal == null || dVal == null) continue;
  fluidLines.push(`  --mpds-space-${step}: ${fluidValue(mVal, dVal)};`);
}
fluidLines.push('');

fluidLines.push('  /* Spacing Aliases */');
for (const [t, n] of Object.entries(TSHIRT)) {
  fluidLines.push(`  --mpds-space-${t}: var(--mpds-space-${n});`);
}
fluidLines.push('');

fluidLines.push('  /* Dimensions */');
for (const step of numericSteps) {
  fluidLines.push(`  --mpds-dimension-${step}: var(--mpds-space-${step});`);
}
fluidLines.push('');

fluidLines.push('  /* Site Layout */');
for (const key of Object.keys(desktop.site)) {
  if (key.startsWith('$')) continue;
  const mVal = mobile.site[key]?.$value;
  const dVal = desktop.site[key]?.$value;
  if (mVal == null || dVal == null) continue;
  fluidLines.push(`  --mpds-site-${key}: ${fluidValue(mVal, dVal)};`);
}

const metricsCss = header('Responsive Metrics — Fluid') +
  `:root {\n${fluidLines.join('\n')}\n}\n`;

// ── Build: Static defaults ───────────────────────────────────────────────────

const elevationCss = `${header('Elevation')}:root {
  --mpds-elevation-0: none;
  --mpds-elevation-1: 0 1px 2px 0 rgba(0, 0, 0, 0.05);
  --mpds-elevation-2: 0 1px 3px 0 rgba(0, 0, 0, 0.1), 0 1px 2px -1px rgba(0, 0, 0, 0.1);
  --mpds-elevation-3: 0 4px 6px -1px rgba(0, 0, 0, 0.1), 0 2px 4px -2px rgba(0, 0, 0, 0.1);
  --mpds-elevation-4: 0 10px 15px -3px rgba(0, 0, 0, 0.1), 0 4px 6px -4px rgba(0, 0, 0, 0.1);
  --mpds-elevation-5: 0 20px 25px -5px rgba(0, 0, 0, 0.1), 0 8px 10px -6px rgba(0, 0, 0, 0.1);
  --mpds-elevation-6: 0 25px 50px -12px rgba(0, 0, 0, 0.25);
}
`;

const radiusCss = `${header('Border Radius')}:root {
  --mpds-radius-none: 0;
  --mpds-radius-sm: 2px;
  --mpds-radius-md: 4px;
  --mpds-radius-lg: 8px;
  --mpds-radius-xl: 12px;
  --mpds-radius-2xl: 16px;
  --mpds-radius-3xl: 24px;
  --mpds-radius-full: 9999px;
}
`;

// ── Build: Icons ─────────────────────────────────────────────────────────────

// Icons needed by components in this project
const ICONS = ['chevron-right', 'arrow-right', 'arrow-left', 'close', 'menu', 'sunrise-2', 'pot-2', 'home'];

function toPascalCase(kebab) {
  return kebab.split('-').map(s => s[0].toUpperCase() + s.slice(1)).join('');
}

function svgToReactComponent(name, svgString) {
  const componentName = toPascalCase(name);

  // Extract all child elements from the SVG as plain attribute objects
  // We use React.createElement to avoid needing JSX transpilation
  const innerMatch = svgString.match(/<svg[^>]*>([\s\S]*?)<\/svg>/i);
  const innerSvg = innerMatch ? innerMatch[1].trim() : '';

  // Parse each child element into a React.createElement call
  const childElements = [];
  const tagRe = /<(\w+)([^>]*?)\/>/g;
  let m;
  while ((m = tagRe.exec(innerSvg)) !== null) {
    const tag = m[1];
    const attrsStr = m[2];
    const attrs = {};
    const attrRe = /(\S+)="([^"]*)"/g;
    let a;
    while ((a = attrRe.exec(attrsStr)) !== null) {
      let key = a[1];
      let val = a[2];
      // camelCase conversions
      key = key.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
      // replace black fills/strokes with currentColor
      if (val === 'black') val = 'currentColor';
      attrs[key] = val;
    }
    childElements.push(`React.createElement('${tag}', ${JSON.stringify(attrs)})`);
  }

  const childrenArg = childElements.length === 1
    ? childElements[0]
    : childElements.length > 1
      ? `[${childElements.join(', ')}]`
      : 'null';

  return `var React = require('react');

function ${componentName}(_ref) {
  var width = _ref.width === undefined ? 24 : _ref.width;
  var height = _ref.height === undefined ? 24 : _ref.height;
  var className = _ref.className;
  var rest = Object.assign({}, _ref);
  delete rest.width; delete rest.height; delete rest.className;
  return React.createElement('svg', Object.assign({ width: width, height: height, viewBox: '0 0 24 24', fill: 'none', 'aria-hidden': 'true', className: className }, rest), ${childrenArg});
}

module.exports = ${componentName};
module.exports.default = ${componentName};
module.exports.${componentName} = ${componentName};
`;
}

function iconTypeDeclaration(name) {
  const componentName = toPascalCase(name);
  return `import * as React from 'react';
export interface IconProps extends React.SVGAttributes<SVGSVGElement> {
  width?: number;
  height?: number;
  className?: string;
}
export declare function ${componentName}(props: IconProps): JSX.Element;
export default ${componentName};
`;
}

console.log('\nBuilding MPDS icons…');
const iconsDir = resolve(DIST, 'icons', 'react');
const iconBarrelExports = [];

for (const iconName of ICONS) {
  const url = `https://raw.githubusercontent.com/matt-plays/mpds/main/icons/svg/${iconName}.svg`;
  try {
    const res = await fetch(url);
    if (!res.ok) { console.warn(`  ✗ ${iconName} (${res.status})`); continue; }
    const svg = await res.text();
    const componentName = toPascalCase(iconName);
    write(resolve(iconsDir, `${componentName}.js`), svgToReactComponent(iconName, svg));
    write(resolve(iconsDir, `${componentName}.d.ts`), iconTypeDeclaration(iconName));
    iconBarrelExports.push(`export { default as ${componentName} } from './${componentName}.js';`);
  } catch (e) {
    console.warn(`  ✗ ${iconName}: ${e.message}`);
  }
}

if (iconBarrelExports.length) {
  write(resolve(iconsDir, 'index.js'), iconBarrelExports.join('\n') + '\n');
  write(resolve(iconsDir, 'index.d.ts'), iconBarrelExports.map(l => l.replace('.js', '')).join('\n') + '\n');
}

// ── Barrels ──────────────────────────────────────────────────────────────────

const barrelCss = `${header('All Tokens')}@import './mpds-colors.css';
@import './mpds-metrics-fluid.css';
@import './mpds-elevation.css';
@import './mpds-radius.css';
`;

const indexCss = `@import './css/mpds.css';\n`;

// ── Write ────────────────────────────────────────────────────────────────────

const cssDir = resolve(DIST, 'css');
const files = [
  [resolve(cssDir, 'mpds-colors.css'),       colorsCss],
  [resolve(cssDir, 'mpds-metrics-fluid.css'), metricsCss],
  [resolve(cssDir, 'mpds-elevation.css'),    elevationCss],
  [resolve(cssDir, 'mpds-radius.css'),       radiusCss],
  [resolve(cssDir, 'mpds.css'),              barrelCss],
  [resolve(DIST,   'index.css'),             indexCss],
];

for (const [path, content] of files) write(path, content);

console.log('\nMPDS tokens built successfully.');
